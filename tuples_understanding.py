# Basics of tuples in python
"""Tuples are a fundamental data structure in Python, 
similar to lists but with some key differences. 
Here's a summary of tuples, their common methods, 
and some exercises to practice:"""

# Summary of Tuples:
"""Tuples are ordered collections of items.
1. They are immutable, meaning once created, their elements cannot be changed.
2. Tuples can contain elements of different types, including other tuples.
3. Tuples are defined using parentheses ()."""

# Common Methods for Tuples:
"""Creating a Tuple:

1. Tuples can be created using parentheses ().
Example: my_tuple = (1, 2, 3, 'hello')

2. Accessing Elements:
Elements in a tuple can be accessed using indexing.
Example: first_element = my_tuple[0]

3. Slicing:
You can slice tuples to extract a subset of elements.
Example: subset = my_tuple[1:3]

4. Length of Tuple:
Use the len() function to get the length of a tuple.
Example: length = len(my_tuple)

5. Concatenation:
Tuples can be concatenated using the + operator.
Example: new_tuple = my_tuple + (4, 5)

6. Repetition:
Tuples can be repeated using the * operator.
Example: repeated_tuple = my_tuple * 3

7. Membership Testing:
Use the in operator to check if an element exists in a tuple.
Example: exists = 'hello' in my_tuple"""

